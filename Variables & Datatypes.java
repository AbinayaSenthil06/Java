// Write a program to Addition, Subtraction, Multiplication, Division, Modulus.

class variable{
    public static void main(String args[])
    {
        // Addition
        int a=10;
        int b=20;
        int c=30;
        int d=a+b+c;
        System.out.println("Addition:"+d);   //output=Addition:60

        // Subtraction
        int e=10;
        int f=20;
        int g=30;
        int h=e-f-g;
        System.out.println("Subtraction:"+h);  //output=Subtraction:-40

        // Multiplication
        int i=10;
        int j=20;
        int k=30;
        int l=i*j*k;
        System.out.println("Multiplication:"+l); //output=Multiplication:6000

        // Division 
        int m=10;
        int n=20;
        int o=30;
        int p=m/n/o;
        System.out.println("Division:"+p);  //output=Division:0

        // Modulus
        int q=10;
        int r=20;
        int s=30;
        int t=q%r%s;
        System.out.print("Modulus:"+t);  //output=Modulus:10

    }

}
